

<h3>Research</h3>

<p>

My current research is focused on software engineering and security for Cyber-Physical Systems (CPS), which are networked systems of cyber (computational and communication) and physical (sensors and actuators) components that interact in a feedback loop with the possibility of human intervention, interaction and utilization. These systems will provide the foundation of our critical infrastructure and have the potential for a profound impact on our daily lives as they form the basis for emerging and future smart services. CPS present new challenges where existing best practices are insufficient, requiring a broad approach with expertise in sensing and hardware, networking, security & privacy, human-computer interaction, and machine learning.

<p>
My research is applied and experimental in nature, so I am looking for students capable of designing and implementing machine learning algorithms and building large scale systems.

