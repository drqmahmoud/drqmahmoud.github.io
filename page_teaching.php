

<h3>Teaching</h3>

My teaching experience spans over 20 years at four higher education institutions in Canada, and two in the UAE. I have also delivered corporate training courses, and several tutorials at international conferences. I started teaching at the university-level in 1996 while I was an M.Sc. student at the University of New Brunswick. My teaching fosters critical thinking and problem solving, and imparts students' lifelong learning skills. I attempt to make students aware of their own abilities by providing a framework for a process-oriented understanding of technical concepts, and guiding them through the development of competence skills that emphasize interdependencies and feedback. Students learn to recognize, and avoid the traps of unstructured or unconnected details.
<p>
My teaching revolves around my life commitment as a second generation educator, to serve students and to effect societal change.

<p>
With the rise of, and somewhat successful, <i>massive open online courses (MOOC)</i>, I strongly believe that the job of the instructor is being transformed from delivering knowledge in the classroom to <i>designing learning experiences</i>. 

<p>
Some of my innovations in teaching include: 
<p>
<ul>

<li><b><a href="https://www.kickstarter.com/projects/sensorian/sensorian-shield-the-physical-world-meets-cloud-co">Sensorian Shield for the Raspberry Pi</a></b>: imagine if the first program to write in an introductory programming course is to display the room's temperature instead of the classical <i>Hello World</i>...and still using a single line of code? That's where the <a href="http://www.nextproject.ca/sensorian/www.sensorian.io/index.html">Sensorian project </a> started.
<li><b><a href="http://nextproject.ca/psw/">Inspiring Youth Through Mobile Technology and Open Source Hardware</a></b>: Mobile app development for high school students and teachers
<li><b><a href="http://scholar.google.ca/citations?view_op=view_citation&hl=en&user=bGLXnHUAAAAJ&cstart=20&citation_for_view=bGLXnHUAAAAJ:g3aElNc5_aQC">Mobile devices in computing education</a></b>: in Summer 2006 I received some second-hand BlackBerry devices through the Toyota Canada recycling program, and in Fall 2006 I started teaching students how to develop apps for such devices. Have a look at <a href="Teaching Mobile Computing to Generation C.pdf">Teaching Mobile Computing to Generation C</a>, the <a href="http://www.nextproject.ca/cmer/cmer-ak/index.html">CMER Academic Kit</a>, <a href="http://www.nextproject.ca/cmer/kit.html">Mobile Web-based App Development</a>, and more <a href="http://www.nextproject.ca/cmer/research.html">related publications</a><br>
 <li><b><a href="http://www.cis.uoguelph.ca/~qmahmoud/hp/">Tablet PCs in project-based courses</a></b>: through an HP Technology Grant for Teaching, I have integrated tablet PCs into some of my courses. Such technology helps in creating an interactive classroom environment with high levels of student engagement. See the article: <a href="Features _ At Guelph.pdf">Tools of Trade to Improve Teaching</a>
 <br>
 <li><b><a href="http://scholar.google.ca/citations?view_op=view_citation&hl=en&user=bGLXnHUAAAAJ&citation_for_view=bGLXnHUAAAAJ:yB1At4FlUx8C">Programming for Poets</a></b>: I have designed a viable technique for teaching introductory computer programming by integrating HTML, JavaScript, and Java in a single course. See also the <a href="http://www.nextproject.ca/cmer/gotomoon">Greenfoot-based scenario for teaching CS1</a> -- this scenario is being used at many high schools around the world.
</ul>

