
<h3>Students</h3>

<p>
On this page you will find information about my contributions to, using <a 
href="http://www.nserc-crsng.gc.ca">NSERC</a> terminology, the training of 
highly qualified personnel (HQP) ranging 
from undergraduate students working on capstone and summer projects to postdoctoral fellows. You 
will also find information about PhD theses for which I have served as an 
external examiner.




<p>
<blockquote>
<hr>
I am always looking for bright and self-motivated graduate students interested in the following research areas: mobile & cloud computing, middleware, and software engineering. If you're interested, email me your CV and a research statement.
<hr>
</blockquote>

